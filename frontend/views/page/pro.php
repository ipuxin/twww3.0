			同时，维护及促进商家公平、开放的竞争环境，把更多优质资源、流量放送给优质商家，特制订本规则，详情请参考
			《拼一下商城商品品质抽检细则》。本规则适用于拼一下商城上所有入驻商家。针对卖家在拼一下商城所售商品的质量、品牌及版权抽检。</p>
