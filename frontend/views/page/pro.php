		<h1>一、商家常见问题</h1>
		<h2>1、拼一下商城是什么平台？</h2>
		<p><em>解答:</em>电子商务未来发展趋势是怎样的呢?关键词有：移动化，平台化，物联网，社交购物，O2O等。而拼一下商城
			顺应发展趋势，致力于打造移动端本地化社交商城，以地级市作为区域元素，通过同城配送，提高配送时效，减少周
			转次数，来提升客户体验。</p>
		<h2>2、如何入驻？</h2>
		<p><em>解答:</em>进入拼一下微信端商城或者APP,点击“个人中心”，点击“我是商家”，通过链接进入拼一下官网（www.
			pingoing.cn）,选择我要开店，流程为：第一，请选择店铺类型；第二、上传相关资质、提交店铺名、店铺LOGO、
			店铺详情等信息；第三、上传新商品。</p>
		<h2>3、入驻企业和个人的区别？</h2>
		<p><em>解答：</em>入驻时选择企业，则要上传企业三证：企业营业执照、组织机构代码、税务登记证。 选择个人入驻，则需要上
			传身份证原件正反面、个人近照及手持身份证的近照。</p>
		<h2>4、经营的产品类目比较多，入驻时只能选择一个主营类目怎么办？</h2>
		<p><em>解答：</em>入驻平台时，先选择重要的主营类目，后期还可申请添加销售其他类目产品，类目一旦选择后，便不能再更改。</p>
		<h2>5、拼一下商城店铺命名规则</h2>
		<p><em>解答：</em>每个城市两个以上店铺申请相同的符合规定的店铺名称，拼一下商城依照申请在先原则核定，其他店铺没有权
			限申请已经通过审核的店铺名称；未经品牌商或生产商授权，店铺名称禁止使用“特约经营”、“特约经销”、“总
			经销”、“总代理”、“厂家认证”、“官方店铺”等可以使买家误认为改店铺已经取得相应品牌授权的表述或内容
			个人卖家店铺名称禁止使用“旗舰”、“专卖”、“专营”、“官方”、“直营”、“官方认证”、“官方授权”等
			表述或内容。</p>
		<h1>二、关于提交资质问题</h1>
		<h2>1、在境外注册的公司，入驻时，怎么填写企业三证？</h2>
		<p><em>解答：</em>境外注册的公司为单证，可以在企业营业执照、组织机构代码、税务登记证、统一社会信用代码一栏全部填写
			境外单证上的代码，上传资质图片时，也统一上传境外单证资质。</p>
		<h2>2、三证合一的商家如何填写营业执照、组织机构代码、税务登记？</h2>
		<p><em>解答：</em>三证合一的商家在填写营业执照、组织机构代码、税务登记时，全部填写税务统一社会信用代码，选择上传照
			片时，也同样全部上传税务统一社会信用代码的资质。 </p>
		<h2>3、未更新为三证合一的企业商家，如何填写统一社会信用代码？</h2>
		<p><em>解答：</em>企业商家还未更新为三证合一，在填写统一信用代码这一栏，填写企业营业执照代码即可，上传税务统一社会
			信用代码的资质图片时，也同样上传企业营业执照。</p>
		<h2>4、提交了入驻信息，会在多久审核？</h2>
		<p><em>解答：</em>平台会在2个工作日内审核，如果通过审核，商家会收到平台发送的审核通过信息。 </p>
		<h2>5、食品类目商家入驻时，还需要向平台提交什么资质？</h2>
		<p><em>解答：</em>食品类目商家入驻时，必须具备食品流通许可证（食品经营许可证），食品生产厂家则需具备食品生产许可证
			或食品卫生许可证，以备公司查验。</p>
		<h2>6、商家入驻时，审核资质不通过，拒绝理由为被列入企业异常经营名录，该怎么办？</h2>
		<p><em>解答：</em>出现该问题时，商家自行到全国企业信用信息公示系统（http://gsxt.saic.gov.cn/）查询被列入异常经营名
			录的原因。若企业被工商局列入异常的问题已得到解决，但由于信息的延迟，全国企业信用信息公示系统未做变更，
			则需要向平台提交所在地工商部门盖章的移出经营异常名录申请表。 </p>
		<h2>7、为什么商家上传资质图片时，总是出现格式错误？</h2>
		<p><em>解答：</em>有两种种情况会上传不了：①图片后缀名不对，图片后缀名是不是与规定的标准格式一致。②图片大小不是规
			定的范围以内。</p>
		<h1>三、关于商家服务规则的疑问</h1>
		<h2>1、发货时间要求？</h2>
		<p><em>解答：</em>拼一下平台商家必须承诺客户在支付成功后24小时内进行发货（对于拼团类别的，需要在拼团成功后24小时内
			进行发货），发货后快递单号要及时导入系统，以物流信息为证。做好用户体验，提高用户满意度，这也是拼一下极
			速发货的特色。</p>
		<h2>2、商家客服在线服务原则</h2>
		<p><em>解答：</em>① 平台要求商家客服在每天9:00—21:00之间需保持在线客服电话畅通接受用户咨询。同时，平台会不定期、不定时
			的对商家客服在线情况进行抽检。<br>② 商家客服需保持良好的服务用语及服务态度，不能出现不礼貌言语，或在沟通中使用文字辱骂、贬低用户的情况，
			包含且不限于人身攻击、侮辱、贬低用户、威胁性语言等情况。如有发现或用户投诉，平台将会严格处理。</p>
		<h2>3、出现售后问题，商家该怎么处理？</h2>
		<p><em>解答：</em>如果客户收到商品出现破损或者与商城介绍不符等售后问题，商家需要与客户积极沟通协商处理，如需商家做
			出售后处理，则依据实际情况可协商选择给客户部分退款或者全额退款。如果商家处理后或者拒绝处理，客户对处理
			结果不满意，客户有权利直接申诉到拼一下官方售后处（进入拼一下微信公众号，在公众号客服处留言即可或者直接
			联系售后服务电话：400-6590-698进行申诉。）</p>
		<h2>4、退货服务相关说明</h2>
		<p><em>解答：</em>生鲜、水果、冻品类，客户收到货后如果有问题的话，需要24小时内找对应商家进行处理协商。如果退货原因
			是因为商家原因（实收货物与商城描述不符或者实收货物有破损等），商家需要根据实际情况进行理赔。如果退货原
			因是因为客户个人需求原因，则不支持无理由退货服务。</p>
